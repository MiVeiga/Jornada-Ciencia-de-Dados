# Em Python, um módulo é basicamente um arquivo que contém código 
# Python reutilizável. Esse código pode incluir funções, classes, 
# variáveis e até mesmo outros módulos. A ideia principal por trás dos 
# módulos é organizar o código em partes separadas, 
# o que torna o desenvolvimento 
# mais fácil, claro e eficiente.


# Primeiro, importamos o módulo math
import math

# Agora, podemos usar as funções matemáticas do módulo math
resultado = math.sqrt(25)  # Retorna a raiz quadrada de 25
print(resultado)  # Isso imprimirá 5.0, a raiz quadrada de 25

angulo = 45
radianos = math.radians(angulo)  # Converte 45 graus para radianos
print(radianos)  # Isso imprimirá 0.7853981633974483, o valor em radianos para 45 graus
